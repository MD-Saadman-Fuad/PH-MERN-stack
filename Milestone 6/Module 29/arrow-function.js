
// function declaration
function add(a, b){
    return a + b;
}
// function expression
const addition = function(a, b){
    return a + b;
}

// arrow function
const adding = (a, b) => a + b;











// calling the functions
console.log(add(5, 3));
console.log(addition(5, 3));
console.log(adding(5, 3));